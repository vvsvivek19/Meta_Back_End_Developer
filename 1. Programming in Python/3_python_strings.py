# Assign a single-line string to variable 'single'
single = "This is a single line"
# Assign a multi-line string to variable 'multiLine' using backslashes to continue the string on the next line
multiLine = "This is too\
 big to fit\
 in a single line so\
 I multilined it"
# Print the value of 'single'
print(single)
# Print the value of 'multiLine'
print(multiLine)

